# HW 7th old enough

age = int(input("How old are you?: "))

if age >= 18:
    print(f"You are {age}, you can vote!")
elif age >= 16:
    print(f"You are {age} you can drive!")
elif age >= 15:
    print(f"You are {age}, you can get your learners permit!")
elif age >= 4:
    print(f"You are {age}, you can go to school!")
